using Castle.DynamicProxy;
using System;
using System.Linq;
using System.Text;

namespace Autofac.Castle.Interceptor.Core.Interceptors
{
    public class AutofacCastleInterceptor : IInterceptor
    {
        /// <summary>
        /// 拦截方法
        /// </summary>
        /// <param name="invocation">包含被拦截方法的信息</param>
        public void Intercept(IInvocation invocation)
        {
            Console.WriteLine("Before Intercept");
            invocation.Proceed();
            Console.WriteLine("After Intercept");
        }
    }
}
